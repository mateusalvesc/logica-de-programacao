let valorA = parseInt(prompt("Digite o primeiro valor inteiro:"));
let valorB = parseInt(prompt("Digite o segundo valor inteiro:"));
let valorC = parseInt(prompt("Digite o terceiro valor inteiro:"));
let valorD = parseInt(prompt("Digite o quarto valor inteiro:"));

let produto = valorA * valorC;
let soma = valorB + valorD;

alert("O resultado do produto de " + valorA + " * " + valorC + " é igual a: " + produto);
alert("O resultado da soma de " + valorB + " + " + valorD + " é igual a: " + soma);