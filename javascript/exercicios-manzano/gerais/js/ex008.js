let comprimento = parseFloat(prompt("Digite a medida do comprimento da caixa em cm:"));
let largura = parseFloat(prompt("Digite a medida da largura da caixa em cm:"));
let altura = parseFloat(prompt("Digite a medida da altura da caixa em cm:"));

let volume = comprimento * largura * altura;

alert("O volume desta caixa corresponde a: " + volume + " cm.");